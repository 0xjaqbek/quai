function searchAddress() {
    const input = document.getElementById('addressInput').value.trim();
    const resultContainer = document.getElementById('resultContainer');
    const errorMessage = document.getElementById('errorMessage');
    const resultBody = document.getElementById('resultBody');

    // Clear previous results
    resultBody.innerHTML = '';
    resultContainer.style.display = 'none';
    errorMessage.style.display = 'none';

    if (!input) {
        errorMessage.textContent = 'Please enter an address';
        errorMessage.style.display = 'block';
        return;
    }

    // Search for the address
    const result = transferData.find(item => 
        item.address.toLowerCase() === input.toLowerCase()
    );

    if (result) {
        // Show result
        resultBody.innerHTML = `
            <tr>
                <td class="mono">${result.address}</td>
                <td class="right">${result.amount}</td>
                <td class="right">${result.percentage}</td>
                <td class="center">${result.lastTransfer}</td>
                <td class="center">${result.transferCount}</td>
            </tr>
        `;
        resultContainer.style.display = 'block';
    } else {
        errorMessage.textContent = 'Address not found in transfer data';
        errorMessage.style.display = 'block';
    }
}

// Optional: Add event listener for Enter key
document.getElementById('addressInput').addEventListener('keypress', function(e) {
    if (e.key === 'Enter') {
        searchAddress();
    }
});
